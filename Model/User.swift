import Foundation

class User{
    var username: String
    var password: String
    var showname: String
    var email: String
    var phonenumber: String
    
    init(username: String, password: String, showname: String, email: String, phonenumber: String){
        self.username = username
        self.password = password
        self.showname = showname
        self.email = email
        self.phonenumber = phonenumber
    }







}
